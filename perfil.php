                <p>
                    O Engenheiro de Computação deve ter uma sólida formação técnico-científica e profissional geral que o capacite a absorver
                    e desenvolver novas tecnologias, estimulando a sua atuação crítica e criativa na identificação e resolução
                    de problemas, considerando seus aspectos político-econômicos, sociais, ambientais e culturais, com visão
                    ética e humanística, em atendimento às demandas da sociedade. Durante sua formação o Engenheiro de Computação
                    deve desenvolver, as seguintes competências e habilidades para o pleno exercício de suas atividades profissionais:

                    </br>

                    <li>Capacidade de conceber e analisar sistemas, produtos e processos nas áreas de software e hardware, utilizando
                        modelos adequados;</li>
                    <li>Capacidade de planejar, supervisionar, elaborar e coordenar projetos na área de Engenharia de Computação;</li>
                    <li> Capacidade de leitura, interpretação e expressão gráfica;</li>
                    <li> Capacidade de síntese, aliada à capacidade de compreensão e expressão oral e escrita em língua portuguesa;</li>
                    <li> Capacidade de compreender os problemas administrativos, legais, sócio-econômicos, culturais e do meio
                        ambiente;
                    </li>
                    <li> Capacidade de gerenciamento, operação e manutenção de sistemas e processos da Engenharia de Computação;</li>
                    <li> Domínio de conceitos, tecnologias e ferramentas necessárias ao exercício da prática da Engenharia de
                        Computação;
                    </li>
                    <li> Capacidade de conduzir e interpretar resultados de atividades práticas e experimentais.</li>

                    </br>

                    O curso de Engenharia de Computação deve desenvolver no estudante um senso crítico e de cidadania, que possibilite a prática
                    das seguintes atitudes durante sua vida profissional:

                    </br>
                    </br>

                    <li> Compromisso com a ética profissional;</li>
                    <li>Responsabilidade social, política e ambiental;</li>
                    <li> Espírito empreendedor: postura pró-ativa e empreendedora;</li>
                    <li> Compreensão da necessidade da permanente busca da atualização profissional.</li>

                    </br>

                    De modo mais específico, o Curso de Engenharia da Computação tem como objetivo preparar engenheiros para atuar nas áreas
                    de hardware e software. Este curso apresenta o seguinte perfil profissional (de acordo com a Comissão
                    de Especialistas de Ensino de Engenharia – CEEEng/SESU/MEC):

                    </br>
                    </br>

                    “O Engenheiro de Computação é um profissional com formação plena em Engenharia, preparado em assuntos de Computação para
                    especificar, conceber, desenvolver, implementar, adaptar, produzir, industrializar, instalar e manter
                    sistemas computacionais, bem como perfazer a integração de recursos físicos e lógicos necessários para
                    o atendimento das necessidades informacionais, computacionais e da automação de organizações em geral.”
